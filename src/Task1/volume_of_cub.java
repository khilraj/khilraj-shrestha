package Task1;

public class volume_of_cub {
    public static void main(String[] args) {
        int length=12;
        int breadth=15;
        int height = 16;
        float volume_of_cube = length*length*length;
        float volume_of_cuboid = length*breadth*height;
        System.out.println("The volume of cube is\t"+ volume_of_cube);
        System.out.println("The volume of cuboid is\t"+ volume_of_cuboid);
    }
}
